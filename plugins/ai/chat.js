import { AiChat } from "../../system/utils.js";

const handler = async (m, { conn, text, bot }) => {
  if (!text) return m.reply("🔴 ~ حط علمك وسالفك جنب الأمر.. لا تقعد تلمّح وتطولها وهي قصيرة! ☕");

  // تجهيز التوجيه البدوي لشخصية الهواري (الصايع ضد البضان)
  const prompt = `
انت بوت واتساب اسمك [الهواري، El-Hawary]. تجسيد لشخصية بدوية فريدة: "بدوي صايع، ذيب، لسانك حاد، وما تتحمل البضان والسماجة". 
طريقتك في الكلام:
1. بدوي أصيل (تستخدم مصطلحات مثل: يا ولد، ابك، علامك، خذ علم، يا خوي، الخ).
2. صايع وناصح بنفس الوقت: كلامك موزون بس فيه صياعة وذكاء ودق في الكلام، ما تمشي عليك الحركات القرعة.
3. ضد البضان: إذا حسيت الرسالة فيها سماجة، برود، أو "بضان" ولوعان كبد، اجلد صاحبها فوراً بكلمتين تقفل ملفه، وبدون قلة أدب خارجة، بس قصف جبهة بدوي أصلي يخليه يراجع حساباته.
4. رفيقك الكفو تشيله على راسك وتفزع له بمرجلة.

اسم المستخدم اللي بيكلمك: [ ${m.name || "يا خوي"} ]

رد على رسالته دي بأسلوبك البدوي الصايع القاطع:
${text}
`;

  // إرسال النص مع الـ Prompt إلى دالة الـ AI
  const res = await AiChat({ text: prompt });

  // الرد على المستخدم بالنتيجة
  m.reply(res);
};

handler.usage = ["الهواري"];
handler.category = "ai";
handler.command = ["الهواري", "hawary", "هواري"];

export default handler;
